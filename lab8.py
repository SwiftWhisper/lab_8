def printAllStudents(students):  # Функцію написав Цебро
    for ПІБ, info in students.items():
        print(f"Прізвище, ім'я, по батькові: {ПІБ}")
        print(f"Номер групи: {info['номер_групи']}")
        print(f"Курс: {info['курс']}")
        print("Предмети та оцінки:")
        for предмет, оцінки in info["предмети_та_оцінки"].items():
            оцінки_str = ", ".join(map(str, оцінки))
            print(f"  {предмет}: {оцінки_str}")
        print("-" * 40)


def addStudent(students): #Функцію написала Ялижко
    ПІБ = input("Введіть ПІБ студента: ")
    номер_групи = input("Введіть номер групи студента: ")
    курс = int(input("Введіть курс студента: "))

    предмети_та_оцінки = {}
    while True:
        предмет = input("Введіть назву предмета (або 'стоп' для завершення): ")
        if предмет.lower() == "стоп":
            break
        оцінки = list(map(int, input(f"Введіть оцінки для предмета {предмет} через кому: ").split(',')))
        предмети_та_оцінки[предмет] = оцінки

    students[ПІБ] = {
        "номер_групи": номер_групи,
        "курс": курс,
        "предмети_та_оцінки": предмети_та_оцінки
    }
    print(f"Студент {ПІБ} успішно доданий.")


